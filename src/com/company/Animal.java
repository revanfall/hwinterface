package com.company;

public class Animal {
    public int id;
    public int age;
    public int weight;
    public String color;

    public Animal() {
    }

    public Animal(int id, int age, int weight, String color) {

        this.id = id;
        this.age = age;
        this.weight = weight;
        this.color = color;
    }
}
